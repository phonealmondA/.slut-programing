    pub fn generate_asteroids_game(
        &mut self,
        program_name: &str,
        variables: &HashMap<String, StoredVariable>
    ) -> Result<()> {
        println!(">> Generating Asteroids game: {}", program_name);
        
        // Extract game-related variables
        self.extract_game_variables(variables);
        
        // Generate complete Rust game program
        self.create_game_project(program_name)?;
        self.generate_main_rs(program_name)?;
        self.generate_game_structs(program_name)?;  // Fixed: pass program_name
        self.generate_game_logic()?;
        self.generate_cargo_toml(program_name)?;
        
        // Attempt to compile the game
        self.compile_game(program_name)?;
        
        println!("** Asteroids game generated successfully!");
        println!("   Location: {}/{}/", self.output_dir, program_name);
        println!("   Executable: {}/{}/target/release/{}.exe", self.output_dir, program_name, program_name);
        
        Ok(())
    }
    
    fn extract_game_variables(&mut self, variables: &HashMap<String, StoredVariable>) {
        println!(">> Extracting game variables from quantum consciousness cache");
        
        for (name, var) in variables {
            if let VariableValue::Number(value) = &var.value {
                self.game_variables.insert(name.clone(), *value);
                println!("   - Extracted: {} = {}", name, value);
            }
        }
        
        // Set defaults for missing variables
        self.set_default_if_missing("screenWidth", 800.0);
        self.set_default_if_missing("screenHeight", 600.0);
        self.set_default_if_missing("playerX", 400.0);
        self.set_default_if_missing("playerY", 300.0);
        self.set_default_if_missing("asteroidCount", 5.0);
        self.set_default_if_missing("maxSpeed", 8.0);
        self.set_default_if_missing("rotationSpeed", 5.0);
    }
    
    fn set_default_if_missing(&mut self, key: &str, default: f64) {
        if !self.game_variables.contains_key(key) {
            self.game_variables.insert(key.to_string(), default);
            println!("   - Default: {} = {}", key, default);
        }
    }
    
    fn create_game_project(&self, program_name: &str) -> Result<()> {
        let project_dir = format!("{}/{}", self.output_dir, program_name);
        fs::create_dir_all(&project_dir)?;
        fs::create_dir_all(format!("{}/src", project_dir))?;
        
        println!("** Created game project directory: {}", project_dir);
        Ok(())
    }
    
