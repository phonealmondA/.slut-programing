fn handle_input(game_state: &mut GameState) {{
    // Thrust
    if is_key_down(KeyCode::Up) || is_key_down(KeyCode::W) {{
        let thrust_x = game_state.player_angle.cos() * 0.5;
        let thrust_y = game_state.player_angle.sin() * 0.5;
        game_state.player_vel_x += thrust_x;
        game_state.player_vel_y += thrust_y;
        
        // Cap maximum speed
        let speed = (game_state.player_vel_x.powi(2) + game_state.player_vel_y.powi(2)).sqrt();
        if speed > 8.0 {{
            game_state.player_vel_x = (game_state.player_vel_x / speed) * 8.0;
            game_state.player_vel_y = (game_state.player_vel_y / speed) * 8.0;
        }}
    }}
    
    // Rotation
    if is_key_down(KeyCode::Left) || is_key_down(KeyCode::A) {{
        game_state.player_angle -= 0.1;
    }}
    if is_key_down(KeyCode::Right) || is_key_down(KeyCode::D) {{
        game_state.player_angle += 0.1;
    }}
    
    // Shooting
    if is_key_pressed(KeyCode::Space) {{
        game_state.shoot_bullet();
    }}
}}

